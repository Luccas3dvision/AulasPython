v  = input('Digite para saber Info primitivos?')
#mostras os primitivos
print('O OBJETO primitivo da desse valor é ',type(v))
print('O OBJETO posui espaços ?', v.isspace())
print('O OBJETO possui numero ?', v.isnumeric())
print('O OBJETO esta em minusculo ?',v.islower())
print('O OBJETO esta em Alfabeto ?',v.isalpha())
print('O OBJETO esta em Maiusculas ?',v.isupper())
print('O OBJEtO esta capitalizada?',v.istitle())